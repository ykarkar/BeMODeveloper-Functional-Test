      >History, rationale, and the structure of the CDA interview <br
    /></span>
    <span style="font: 16px Arial, Verdana, Helvetica, sans-serif"
      ><br />Recall from our discussion above that we said an effective
      interview process is the most reliable way to select candidates who
      perform well clinically. Well in an attempt to test this theory, in 2004,
      Smithers et al. conducted a study, which produced results that were so
      shocking, that it unequivocally reinforced the Canadian Dental
      Association's earlier decision to commission a "new structured interview
      based on state-of-the-art contemporary interview techniques" (i.e. CDA
      structured interview) <br /><br />What were these shocking results you may
      ask? The evidence gathered by Smithers et al. (2004) simply reinforced
      earlier suspicions about the ineffectiveness of traditional interview
      processes. They showed that, "a typical [traditional] admissions interview
      was in fact worse than neutral in that it was negatively associated with
      students' performance in the first year of dental training, did not
      predict academic performance, and may have led to poor selection
      decisions." Thus, it should come as no surprise that the traditional panel
      interview has been replaced by most dental school with the CDA structured
      interview, which is a more reliable and valid future predictor of clinical
      performance. <br /><br />The CDA interview was not only re-structured in
      its format of delivery, but it was also restructured in terms of the type
      of questions that would be ask, and how they would be rated or scored by
      the interviewers. Let us first talk about the type of questions that you
      may encounter on your CDA structured interview. <br /><br /><br
    /></span>
    <span
      style="
        font: 16px Arial, Verdana, Helvetica, sans-serif;
        font-weight: bold;
        font-weight: bold;
      "
      >Types of Questions <br
    /></span>
    <span style="font: 16px Arial, Verdana, Helvetica, sans-serif"
      ><br />The types of questions that you may be asked during your dental
      school interview can be divided into two categories: (1) Situational
      Interview (SI) questions and (2) Patterned Behaviour Descriptive Interview
      (PBDI) questions. SI questions are those in which the candidates is placed
      in a hypothetical situations (i.e. vignette) and is asked what they would
      react in that given situation. For example, <br /><br />"You are
      babysitting your sisterâ€™s young child, who is nervous and upset about his
      mother being away. You are trying to calm him down and offer him some ice
      cream. As you are dishing out the ice cream, the child bites down hard on
      your hand. How would you react?" <br /><br />Conversely, PBDI type
      questions, ask the candidates "about past behaviour with the assumption
      that past behaviour is the best predictor of future behaviour." An example
      of a PBDI type questions is: <br /><br /><br />Many of us have had to deal
      with juggling busy schedules. Think of a time in the past when an
      important but unscheduled situation arose that required your attention,
      but you had a number of prior commitments on your agenda. What did you do?
      What was the outcome? <br /><br />Notice how SI questions are typically
      future-oriented, as opposed to PBDI questions, which are past-oriented.
      The specific and actual SI and PBDI questions are devised according to
      seven competencies, that the CDA has found to be reliable and valid
      indicators of future performance. In other words, every question that is
      asked during a dental school interview, regardless of being a SI or PBDI
      question, will address one or more than one of the seven competencies.
      <br /><br
    /></span>
    <span
      style="
        font: 16px Arial, Verdana, Helvetica, sans-serif;
        font-weight: bold;
        font-weight: bold;
      "
      >The Seven Competencies<br
    /></span>
    <span style="font: 16px Arial, Verdana, Helvetica, sans-serif"
      ><br />1. Communication: does the applicant have excellent communication
      skills?<br />2. Conscientiousness: is the applicant thorough, careful to
      do tasks well?<br />3. Integrity : is the applicant honest with themselves
      and others?<br />4. Judgment and analysis: does the applicant have the
      capability to make sound judgments? Do they gather all the facts before
      making a decision?<br />5. self-control : Does the applicant remain calm
      and in control in difficult situations?<br />6. sensitivity to others :
      Does the applicant show empathy towards others? Do they take the feelings
      of others into consideration?<br />7. Tact and diplomacy : Does the
      applicant show sensitivity in dealing with difficult issues? Does the
      applicant possess the necessary skills to deal with others without causing
      negative feelings?<br /><br />Notice in the above examples that the SI
      sample question is addressing the competencies of self-control,
      sensitivity to others, communication, while the PBDI question addresses
      the competencies of conscientiousness, Integrity, and judgement and
      analysis. In all of the questions that will be asked of you during your
      interview, the competency of communication is a constant that is
      continuously tested and retested. In order to be successful, however, you
      will have to be able to know which other competencies also apply to the
      question so that you can formulate an appropriate response, which touches
      on the key factors essential for the interviewers. <br /><br /><br
    /></span>
