      >What is the purpose of the dental school interview?<br />
    </span>
    <span style="font: 16px Arial, Verdana, Helvetica, sans-serif"
      ><br />Regardless of the format of dental school interview (e.g. CDA
      structured interview, MMI, or Panel interview), the purpose of the
      interview is rather straightforward and remains constant across the board:
      to assess the personality and Non-Cognitive Skills (NCSs) of the
      candidate. <br /><br />What are NCSs? By these we mean the following:
      Communication skills, interpersonal skills, ethical and moral decision
      making capacity, maturity, professionalism, sense of social
      responsibility, service to community, leadership, initiative, scholarship,
      ability to collaborate with others, conflict resolution skills, etc.
      <br /><br />Research has shown that, although academic performance (i.e.
      GPA and DAT scores) is a great indicator of didactic abilities in the
      first and second years of dental school, it provides, however, a very poor
      predictive value when it comes to future clinical performance. In fact,
      research shows that, an effective interview process is the best indicator
      of future clinical performance in the upper years, as it gives insight
      into the characteristics of the candidate and whether or not there will be
      a likelihood of future behavioural problems (an issue that dental schools
      constantly encounter and struggle to overcome). For example, it has been
      shown that those candidates who are "conscientious" and "open to new
      experiences" perform more effectively in the third and fourth years of
      dental school studies, where the education takes place in a clinical
      setting for the most part. <br /><br />Thus, dental schools, much like
      other professional schools, have over the past decade spent a lot of
      resources to devise the most effective interview process that will give
      them insight into the NCSs of their future candidates. And of course, for
      Canadian dental schools the answer has been the Canadian Dental
      Association's structured interview or CDA structured interviews.
      <br /><br /><br
